def ceaser(direction, shift, message):
    text = ""
    if direction == "decode":
      shift *= -1
      shift = shift % 26
    for char in message:
      if char in alphabet:
        position = alphabet.index(char)
        newposition = position + shift
        text += alphabet[newposition]
      else:
        text += char
    print(f"The {direction}d message is: \n {text}")

